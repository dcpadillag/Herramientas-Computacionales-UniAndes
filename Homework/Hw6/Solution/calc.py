class calculator:
    '''
    This class is a basic calculator, it
    sums, substracts, multiply, divide and exponentiates.
    '''
    def sum(self,a,b):
        a=float(a)
        b=float(b)
        return a+b
    def sub(self,a,b):
        a=float(a)
        b=float(b)
        return a-b
    def div(self,a,b):
        a=float(a)
        b=float(b)
        return a/b
    def mult(self,a,b):
        a=float(a)
        b=float(b)
        return a*b
    def pow(self,a,b):
        a=float(a)
        b=float(b)
        return a**b
